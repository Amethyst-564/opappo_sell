import lombok.extern.slf4j.Slf4j;
import org.springframework.beans.BeanUtils;
import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.data.domain.Page;
import org.springframework.data.domain.PageImpl;
import org.springframework.data.domain.Pageable;
import org.springframework.stereotype.Service;
import org.springframework.util.CollectionUtils;
import pers.opappo.sell.converter.OrderMaster2OrderDTOConverter;
import pers.opappo.sell.dataobject.OrderDetail;
import pers.opappo.sell.dataobject.OrderMaster;
import pers.opappo.sell.dataobject.ProductInfo;
import pers.opappo.sell.dto.CartDTO;
import pers.opappo.sell.dto.OrderDTO;
import pers.opappo.sell.enums.OrderStatusEnum;
import pers.opappo.sell.enums.PayStatusEnum;
import pers.opappo.sell.enums.ResultEnum;
import pers.opappo.sell.exception.SellException;
import pers.opappo.sell.repository.OrderDetailRepository;
import pers.opappo.sell.repository.OrderMasterRepository;
import pers.opappo.sell.service.*;
import pers.opappo.sell.utils.KeyUtil;

